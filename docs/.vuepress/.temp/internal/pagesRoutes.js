import { Vuepress } from '@vuepress/client'

const routeItems = [
  ["v-8daa1a0e","/",{"title":""},["/index.html","/README.md"]],
  ["v-7554641c","/feature/",{"title":"功能"},["/feature/index.html","/feature/README.md"]],
  ["v-fffb8e28","/guide/",{"title":"a"},["/guide/index.html","/guide/README.md"]],
  ["v-d8bfacea","/setup/",{"title":""},["/setup/index.html","/setup/README.md"]],
  ["v-3706649a","/404.html",{"title":""},["/404"]],
]

export const pagesRoutes = routeItems.reduce(
  (result, [name, path, meta, redirects]) => {
    result.push(
      {
        name,
        path,
        component: Vuepress,
        meta,
      },
      ...redirects.map((item) => ({
        path: item,
        redirect: path,
      }))
    )
    return result
  },
  [
    {
      name: '404',
      path: '/:catchAll(.*)',
      component: Vuepress,
    }
  ]
)
